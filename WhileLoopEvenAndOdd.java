package HomwWorkWeek13;

public class WhileLoopEvenAndOdd {
    public static void main(String[] args) {
        int a = 0;
        while (a < 20) {
            if (a % 2 == 0) {
                System.out.println(a);
        }
        a++; }// Print 10 even number

        int i = 0;
        while (i < 20) {
            if (i % 2 == 1) {
                System.out.println(i);
            }
            i++; }// Print 10 odd number

    }
}


















